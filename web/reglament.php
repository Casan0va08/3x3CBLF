            <h2>Reglament del 3X3</h2>
        </div>

        <!-- REGLAS PRINCIPALES -->
        <div class="rules-section">
            <h3><i class="fas fa-gavel"></i> Normes del Joc</h3>
            
            <div class="rule-item">
                <div class="rule-number">1</div>
                <div class="rule-content">
                    Si a l'hora de començar no hi ha 3 jugadors a pista, es considerarà partit perdut per <strong>Incompareixença</strong>.
                </div>
            </div>

            <div class="rule-item">
                <div class="rule-number">2</div>
                <div class="rule-content">
                    Tots els jugadors han de portar la <strong>samarreta del mateix color</strong>, i tenir una reserva.
                </div>
            </div>

            <div class="rule-item">
                <div class="rule-number">3</div>
                <div class="rule-content">
                    Els partits duraran <strong>10 minuts a temps corregut</strong>, o fins que un dels 2 equips arribi als <strong>21 punts</strong>.
                </div>
            </div>

            <div class="rule-item">
                <div class="rule-number">4</div>
                <div class="rule-content">
                    La primera possessió es decidirà per <strong>salt entre dos</strong>.
                </div>
            </div>

            <div class="rule-item">
                <div class="rule-number">5</div>
                <div class="rule-content">
                    Els atacs comencen quan el defensor entrega la pilota a l'equip atacant fora de la línia de triple (<strong>CHECK-BALL</strong>).
                </div>
            </div>

            <div class="rule-item">
                <div class="rule-number">6</div>
                <div class="rule-content">
                    Quan l'equip defensor recuperi la pilota, haurà de <strong>sortir de la línia de 3</strong> per poder atacar.
                </div>
            </div>

            <div class="rule-item">
                <div class="rule-number">7</div>
                <div class="rule-content">
                    Els atacs tenen una durada màxima de <strong>12 segons</strong>, a comptar des que es produeix:
                    <ul class="sub-rules">
                        <li>el check-ball</li>
                        <li>un rebot ofensiu</li>
                        <li>una falta</li>
                        <li>algun motiu atribuïble a l'equip que està defensant</li>
                        <li>fora de banda</li>
                    </ul>
                </div>
            </div>

            <div class="rule-item">
                <div class="rule-number">8</div>
                <div class="rule-content">
                    <strong>Puntuació:</strong> Dins de la línia de triple (6,25) val <span class="points">1 punt</span>; fora val <span class="points">2 punts</span>.
                </div>
            </div>

            <div class="rule-item">
                <div class="rule-number">9</div>
                <div class="rule-content">
                    <strong>Faltes de tir:</strong> 1 o 2 tirs lliures en funció de si s'estava tirant de dins o de fora de la línia de triple.
                </div>
            </div>

            <div class="rule-item">
                <div class="rule-number">10</div>
                <div class="rule-content">
                    Els <strong>canvis</strong> es faran abans d'un Check-ball o d'un tir lliure.
                </div>
            </div>

            <div class="rule-item">
                <div class="rule-number">11</div>
                <div class="rule-content">
                    <strong>Acumulació de faltes:</strong>
                    <ul class="sub-rules">
                        <li>a partir de la <strong>setena</strong>, es penalitzaran la falta amb 2 tirs lliures</li>
                        <li>a partir de la <strong>desena</strong>, 2 tirs lliures més possessió</li>
                    </ul>
                </div>
            </div>

            <div class="rule-item">
                <div class="rule-number">12</div>
                <div class="rule-content">
                    <strong>No hi ha temps morts.</strong>
                </div>
            </div>

            <div class="rule-item">
                <div class="rule-number">13</div>
                <div class="rule-content">
                    Si al final del temps de joc el tempteig està <strong>empatat</strong>, es disputarà una pròrroga. El primer equip que anoti <strong>UN punt</strong> a la pròrroga guanyarà el partit.
                </div>
            </div>
        </div>

